#include <iostream>
using namespace std;

int main(){
  // tipe data
  // bilangan bulat
  int satu = 10;
  short dua = 20;
  long tiga = 30;
  
  // bilangan decimal, pecahan
  float empat = 1.1;
  double lima = 2.1;
  
  // character
  char enam = '$';
  
  // bilangan boolean (true/false)
  bool tujuh = true;
  
  cout << "integer = " << satu << endl;
  cout << "short = " << dua << endl;
  cout << "long = " << tiga << endl;
  
  cout << "float = " << empat << endl;
  cout << "double = " << lima << endl;
  
  cout << "char = " << enam << endl;
  
  cout << "boolean = " << tujuh << endl;
  cin.get();
  return 0;
}